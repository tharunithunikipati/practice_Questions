package operators;
import java.util;










class Assignment {
	public static void main(String args[]) {
		int a=2;
		System.out.println("a= :"+a);
		a+=3;
		System.out.println("a+=2 :"+a);
		a-=2;
		System.out.println("a-=2 :"+a);
		a*=4;
		System.out.println("a*=4 :"+a);
		a/=5;
		System.out.println("a/=5 :"+a);
		a%=6;
		System.out.println("a%=6 :"+a);
	}
}













































