package operators;

public class operators {
	public static void main(String args[])
	{
		int a=1;
		int b=2;
		System.out.println("A + B =:"+(a+b));
		System.out.println("A - B =:"+(a-b));
		System.out.println("A * B =:"+(a*b));
		System.out.println("A / B =:"+(a/b));
		System.out.println("A % B =:"+(a%b));
	}
}


